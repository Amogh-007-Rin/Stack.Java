/**
 * HelloWorldCommented prints a greeting to the console.
 * This class demonstrates the use of Javadoc comments.
 */
public class HelloWorldCommented {
    public static void main(String[] args) {
        /*
           This is a multi-line comment.
           The main method is the entry point of every Java application.
           It is called by the JVM when the program starts.
        */
        System.out.println("Hello, World!");  // single-line comment
    }
}
